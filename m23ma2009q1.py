# ----------- Manual Cosine Function -----------
def cosine_similarity(v1, v2):

    dot_product = np.dot(v1, v2)
    # do vectors ka dot product calculate kar rahe hain

    norm_v1 = np.linalg.norm(v1)
    norm_v2 = np.linalg.norm(v2)
    # yaha dono vectors ka magnitude (length) nikal rahe hain

    return dot_product / (norm_v1 * norm_v2)
    # cosine similarity formula apply kiya


# Function to Find Top 5 Neighbors
def get_top5_neighbors(word, model):

    if word not in model.wv:
        print(f"{word} not in vocabulary")
        return

    target_vector = model.wv[word]
    similarities = []
    # is list me sab words ki similarity store hogi

    for other_word in model.wv.index_to_key:
        if other_word != word:
            other_vector = model.wv[other_word]

            sim = cosine_similarity(target_vector, other_vector)
            # manually cosine similarity compute kar rahe hain

            similarities.append((other_word, sim))
            # word aur uski similarity value store kar di

    # Sort by similarity descending
    similarities = sorted(similarities, key=lambda x: x[1], reverse=True)
    # highest similarity wale words upar aa jayenge

    print(f"\nTop 5 nearest neighbors for '{word}':")

    for w, s in similarities[:5]:
        print(w, round(s, 4))
        # top 5 most similar words print kar rahe hain


