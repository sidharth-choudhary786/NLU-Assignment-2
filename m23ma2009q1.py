#  Analogy Function
def analogy(word_a, word_b, word_c, model):

    if word_a not in model.wv or word_b not in model.wv or word_c not in model.wv:
        print("One of the words not in vocabulary")
        return
        # agar koi word model ki vocabulary me nahi hai to function stop ho jayega

    vec_a = model.wv[word_a]
    vec_b = model.wv[word_b]
    vec_c = model.wv[word_c]
    # yaha teenon words ke embedding vectors nikal rahe hain

    # vB - vA + vC
    target_vector = vec_b - vec_a + vec_c
    # analogy rule apply kiya (classic word embedding trick)

    similarities = []

    for word in model.wv.index_to_key:
        # model ki vocabulary ke sabhi words par loop
        if word not in [word_a, word_b, word_c]:

            vec = model.wv[word]
            sim = cosine_similarity(target_vector, vec)
            # target vector aur current word ke vector ke beech similarity calculate

            similarities.append((word, sim))
            # word aur similarity value list me store kar di

    similarities = sorted(similarities, key=lambda x: x[1], reverse=True)
    # highest similarity wale words top par aa jayenge

    print(f"\nAnalogy: {word_a} : {word_b} :: {word_c} : ?")

    for w, s in similarities[:5]:
        print(w, round(s, 4))
        # top 5 most similar candidate words print kar rahe hain


